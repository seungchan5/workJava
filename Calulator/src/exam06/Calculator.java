package exam06;

public class Calculator {
	
	/**
	 * 정사각형의 넓이
	 * @param width 
	 * @return width * width
	 */
	public double areaRectangle(double width) {
		return width * width;
	}
	
	/**
	 * 직사각형의 넓이
	 * @param width,height
	 * @return width*height
	 */
	public double areaRectangle(double width, double height) {
		return width * height;
	}
	
	
	
	
	
	
}
